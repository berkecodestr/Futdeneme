let pid = 0
function p(
  name: string,
  overall: number,
  position: Position,
  nationKey: keyof typeof NATIONS,
  team: string,
  teamType: 'national' | 'club',
  era: Era,
  rarity: Rarity,
  stats: [number, number, number, number],
): Player {
  pid += 1
  return {
    id: `p${pid}`,
    name,
    overall,
    position,
    nation: NATIONS[nationKey],
    team,
    teamType,
    era,
    rarity,
    pace: stats[0],
    shooting: stats[1],
    passing: stats[2],
    defending: stats[3],
  }
}

